# 두 수의 합
def add(num1, num2):
    answer = num1 + num2   
    return answer

# 두 수의 차
def sub(num1, num2):
    answer = num1 - num2
    return answer

# 두 수의 곱
def mul(num1, num2):
    answer = num1 * num2
    return answer

# 두 수의 나눗셈 --> 나눗셈 결과의 몫은 //, /은 단순 나눗셈(float형)
def div(num1, num2):
    answer = num1 // num2
    return answer

# 나눗셈 2
def div2(num1, num2):
    return int(num1 / num2 * 1000)

# 나머지 구하기
def remainder(num1, num2):
    return num1 % num2

